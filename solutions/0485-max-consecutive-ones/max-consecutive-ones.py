class Solution:
    def findMaxConsecutiveOnes(self, nums):
        if 1 not in nums: 
            return 0

        consecutiveRecord = 0
        currentCount = 0

        for digit in nums:
            if digit == 0:
                currentCount = 0
            else:
                currentCount += 1
                if currentCount > consecutiveRecord:
                    consecutiveRecord = currentCount

        return consecutiveRecord
        


